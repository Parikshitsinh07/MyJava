//4 Demonstration Of increament and Decrement Operator

class IncrementDecrement{
    public static void main(String[] agrs) {
		int i;
		i=0;
		
		System.out.println(++i);//Print 1
		System.out.println(i++);//Print 1
		System.out.println(i);//Print 2
		System.out.println(--i);//Print 1
		System.out.println(i--);//Print 1
		System.out.println(i);//Print 0
		
	}
}